package com.likelion.project03.week2.day4;

/* NOT 연산자
!true = false
!false = true
 */
public class NotOperatorEx {
    public static void main(String[] args) {
        boolean result = !true;
        System.out.printf("%b\n", result);
        result = !false;
        System.out.printf("%b\n", result);
    }
}
