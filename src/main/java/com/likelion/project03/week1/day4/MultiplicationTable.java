package com.likelion.project03.week1.day4;

public class MultiplicationTable {
    public static void main(String[] args) {
        // 2 * 1 = 2
        // 2 * 2 = 4
        System.out.println("2 " + "* " + "1 " + "= " + "2"); // ln : 전부 적어야 함
        System.out.printf("%d * %d = %d\n", 2, 1, 2); // f : 서식으로 가능
        System.out.printf("%d * %d = %d", 2, 2, 4);
    }
}
